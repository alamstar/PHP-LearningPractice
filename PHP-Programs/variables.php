<?php

	// Variable names must have a "$" in the beginning.
	// After "$" the variable name must start with a letter or "_" character.
	// eg:- $1one = 12; --> this will give a syntax error.
	// eg:- $one1, $o1one, $_one --> these are fine.
	
	// Case-sensitivity: In case of variables, $x is not same as $X.
	
	// echo vs print:-- echo returns nothing whereas print returns 1.
	
	$firstName = "Mohd Naved";
	$lastName = "Alam";
	echo "My name is " . $firstName . " " . $lastName . ".";

